package com.github.jiahut.jackson;

public class Peach implements Fruit {
    private String name;
    private int weight;

    public void setName(String name) {
        this.name = name;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }


    public String getName() {
        return name;
    }

    public int getWeight() {
        return weight;
    }

}
